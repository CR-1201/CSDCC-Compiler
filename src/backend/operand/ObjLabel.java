package backend.operand;

public class ObjLabel extends ObjOperand{
    private String name;

    public ObjLabel(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return this.name;
    }
}
